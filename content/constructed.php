<h1>Constructed Play</h1>

<p>You will be able to play The Space War in a constructed format, both offline and online. <a href='/account/deck'>Create your constructed deck online</a> for offline printing or online play (coming soon).</p>

<p>These are the proposed rules:</p>

<ol>
    <li>You may include any published cards from any of the decks to make your own unique deck.</li>
    <li>Maximum of 3 copies of each card except Drone which you can have 10 copies of.</li>
    <li>Select any commander for your deck.</li>
    <li>Deck size must be between 50 and 70 cards.</li>
    <li>Proxies (cards printed from this site) are ok when playing offline, even in official tournaments.</li>
</ol>

<p>Restricted cards:</p>

<ul>
    <li>None</li>
</ul>

<p>These rules are not yet finalized.</p>

