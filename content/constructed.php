<h1>Constructed Play</h1>

<p>Constructed means that you play with your own deck that you have created.</p>

<p>You can play The Space War in a constructed format, both offline and online (online coming soon). <a href='/account/deck'>Create your constructed decks on this page</a>. See also <a href='/match'>Rules for an Official Match</a>.</p>

<h2>Constructed Rules</h2>

<ol>
    <li>You may include any published cards from any of the decks to make your own unique deck.</li>
    <li>Maximum of 3 copies of each card except Drone which you can have 10 copies of.</li>
    <li>Select any commander for your deck.</li>
    <li>Deck size must be between 50 and 70 cards.</li>
    <li>Proxies (cards printed from this site) are ok when playing, even in official tournaments (as long as card sleeves are used).</li>
    <li>A player cannot win during the first round of the game. All station cards can be flipped but in the next turn the player put down a new station card and has one remaining life.</li>
</ol>

<p>Restricted cards:</p>

<ul>
    <li>None</li>
</ul>

<h2>Changelog During Playtesting</h2>

<ul>
    <li>2023-03-05: New rule added that a player cannot win during the first round of the game.</li>
    <li>2020-12-08: First version of these rules.</li>
</ul>

<p>These rules could be changed in the future depending on the health of the meta.</p>


